# Declare and initialize variables
single_quote = '\''
double_quote = "\""
escape_backslash = "\\"
escape_newline = "\n"
escape_tab = "\t"

# Print the variables
print(single_quote)  # Prints: '
print(double_quote)  # Prints: "
print(escape_backslash)  # Prints: \
print(escape_newline)  # Prints a new line
print(escape_tab)  # Prints a tab