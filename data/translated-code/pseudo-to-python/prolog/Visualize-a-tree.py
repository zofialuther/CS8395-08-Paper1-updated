def display_tree(Direction):
    A = 'Display tree ' + Direction
    D = window(A)
    D.setSize(350, 200)
    T = tree('Root')
    T.setNeighbourGap(10)
    S1 = node('Child1')
    S2 = node('Child2')
    T.addSon(S1)
    T.addSon(S2)
    S11 = node('Grandchild1')
    S12 = node('Grandchild2')
    S1.addSon(S11)
    S1.addSon(S12)
    S21 = node('Grandchild3')
    S22 = node('Grandchild4')
    S2.addSon(S21)
    S2.addSon(S22)
    T.setDirection(Direction)
    D.display(T)
    D.open()