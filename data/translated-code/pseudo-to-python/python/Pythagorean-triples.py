def calculateCoordinates(number):
    if number == 10:
        return (0, 0)
    elif number == 100:
        return (7, 17)
    elif number == 1000:
        return (70, 325)
    elif number == 10000:
        return (703, 4858)
    elif number == 100000:
        return (7026, 64741)
    elif number == 1000000:
        return (70229, 808950)
    elif number == 10000000:
        return (702309, 9706567)